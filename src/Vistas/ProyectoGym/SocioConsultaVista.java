    private void ModificarBotonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarBotonActionPerformed
                                                             
    int filaSeleccionada = jTable1.getSelectedRow();
    if (filaSeleccionada != -1) {
        int opcion = JOptionPane.showConfirmDialog(this, "¬øEst√°s seguro de modificar este socio?", "Confirmar Modificaci√≥n", JOptionPane.YES_NO_OPTION);
        if (opcion == JOptionPane.YES_OPTION) {
            int idSocio = (int) jTable1.getValueAt(filaSeleccionada, 0);
            String dni = (String) jTable1.getValueAt(filaSeleccionada, 1);
            String nombre = (String) jTable1.getValueAt(filaSeleccionada, 2);
            String apellido = (String) jTable1.getValueAt(filaSeleccionada, 3);
            int edad = Integer.parseInt(jTable1.getValueAt(filaSeleccionada, 4).toString());
            String correo = (String) jTable1.getValueAt(filaSeleccionada, 5);
            String telefono = (String) jTable1.getValueAt(filaSeleccionada, 6);
            int asistencia = Integer.parseInt(jTable1.getValueAt(filaSeleccionada, 7).toString());

            Socio socio = new Socio(idSocio, dni, nombre, apellido, edad, correo, telefono);
            socio.setContador_asistencia(asistencia);
            socioData.modificarSocio(socio);
            
            
            jTable1.setValueAt(dni, filaSeleccionada, 1);
            jTable1.setValueAt(nombre, filaSeleccionada, 2);
            jTable1.setValueAt(apellido, filaSeleccionada, 3);
            jTable1.setValueAt(edad, filaSeleccionada, 4); 
            jTable1.setValueAt(correo, filaSeleccionada, 5);
            jTable1.setValueAt(telefono, filaSeleccionada, 6);
            jTable1.setValueAt(asistencia, filaSeleccionada, 7);
        }
    } else {
        JOptionPane.showMessageDialog(this, "Selecciona un socio de la tabla para modificar.", "Error", JOptionPane.ERROR_MESSAGE);
    } 
    }//GEN-LAST:event_ModificarBotonActionPerformed

